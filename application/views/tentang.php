                    <p class="large text-muted">
                    Sudah saatnya sumberdaya informasi dipandang sebagai suatu sumberdaya yang penting, yang sejajar perlakuannya dengan sumberdaya alam dan sumberdaya manusia yang telah lama diagung-agungkan sebagai komponen produksi. Kini kekuatan suatu organisasi, institusi, bisnis, hingga proses manufaktur, ditentukan oleh kemampuannya dalam mengelola ketiga sumberdaya tersebut.<br><br>

                    STMIK KHARISMA Makassar yang secara resmi berdiri pada tanggal 29 Maret 2000, cikal-bakalnya telah dirintis sejak tahun 1991, dan telah berkonsentrasi penuh dalam usaha mendidik dan menghasilkan sumberdaya manusia, berupa tenaga-tenaga profesional sebagai pengelola hingga penanggung jawab pengelolaan sumberdaya informasi.<br><br>

                    STMIK KHARISMA Makassar saat ini telah menghasilkan ribuan alumni dari dua program studinya, yaitu program studi Sistem Informasi dan program studi Teknik Informatika, pada jenjang pendidikan S1, D-III, dan D-I. Sebagian besar alumni telah terserap dalam dunia kerja dan tidak sedikit pula yang menjadi pengusaha yang mandiri.<br><br>

                    Untuk lebih memperluas peran STMIK KHARISMA Makassar dalam menghasilkan sumberdaya manusia yang berkualifikasi sebagai pengelola sumberdaya informasi handal, serta untuk memberikan kesempatan kepada mahasiswa mengembangkan diri dan lebih berfokus pada bidang yang diminatinya, pada kurikulum STMIK KHARISMA Makassar, telah disusun sejumlah program peminatan (konsentrasi) dalam setiap program studi di semua jenjang, diantaranya: peminatan sistem informasi manajemen, sistem informasi akuntansi, rekayasa perangkat lunak, jaringan komputer, peminatan grafik komputer dan multimedia. Peminatan ini diharapkan dapat memberikan penguasaan dan penguatan yang mendalam kepada mahasiswa atas bidang kajian yang dipilihnya. Tidak ketinggalan pula bekal entrepreneurship sebagai kompetensi tambahan yang berguna jika para alumni memilih jalur berwirausaha.<br><br>

                    Dalam upaya mencapai visi yang telah ditetapkan, STMIK KHARISMA Makassar sejak berdirinya tidak pernah berhenti berbenah dan mengembangkan fasilitas serta pelayanan, guna pencapaian kualitas pelaksanaan pendidikan yang berimplikasi pada kualitas alumni. Sejumlah fasilitas seperti ruang kelas dan laboratorium yang nyaman dengan LCD display, WiFi hotspot, dan akses internet, dapat dinikmati mahasiswa yang juga mendukung proses penyelesaian studinya.<br><br>

                    Dengan semua upaya sungguh-sungguh yang telah dilakukan, untuk meningkat-kan kualitas pelayanan, kualitas pelaksanaan pendidikan, kualitas tenaga pengajar, hingga kualitas alumni selama ini, program-program studi di STMIK KHARISMA Makassar telah mendapatkan pengakuan dari Direktorat Pendidikan Tinggi Depdiknas, dengan perpanjangan izin operasional, dan mendapatkan status terakreditasi dari Badan Akreditasi Nasional Perguruan Tinggi. <br><br><br>
